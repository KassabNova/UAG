void menuSelect(void)
{
    int choice = 1;
    int loop = 0;
    int tecla = 0;

    do
    {
        tecla = getch();
        if (tecla==13)
        {
            switch(choice)
            {
                case 1:
                {
                    break;
                }
                case 2:
                {
                    break;
                }
                case 3:
                {
                    tecla=1;
                    break;
                }
                case 4:
                {
					Sleep(100);
					loop = 1;
                    break;
                }
                default:
                {
                    break;
                }
            }
        }
        switch(choice)
        {
            case 1:
            {

                if(tecla==80)
                {
                    clrscr();
                    menuCuerpoAbrir();
                    choice++;
                    break;
                }
                break;
            }
            case 2:
            {
                if(tecla==80)
                {
                    clrscr();
                    menuCuerpoModificar();
                    choice++;
                    break;
                }
                if(tecla==72)
                {
                    clrscr();
                    menuCuerpoRegistro();
                    choice--;
                    break;
                }
                break;
            }
            case 3:
            {
                if(tecla==80)
                {
                    clrscr();
                    menuCuerpoCerrar();
                    choice++;
                    break;
                }
                if(tecla==72)
                {
                    clrscr();
                    menuCuerpoAbrir();
                    choice--;
                    break;
                }
                break;
            }
            case 4:
            {
                if(tecla==72)
                {
                    clrscr();
                    menuCuerpoModificar();
                    choice--;
                }
                break;
            }
            default:
            {
                printf("error en algo");
                break;
            }
        }

    }while(loop==0);

    return;
}

