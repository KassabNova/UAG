void menuSelect(void)
{
    int choice = 1;
    int loop = 0;
    int tecla = 0;

    do
    {
        tiempoActual();
        tecla = getch();
        if (tecla==13)
        {
            switch(choice)
            {
                case 1:
                {
                    printf("\n\r%i",choice);
                    break;
                }
                case 2:
                {
                    printf("\n\r%i",choice);
                    break;
                }
                case 3:
                {
                    printf("\n\r%i",choice);
                    break;
                }
                case 4:
                {
                    printf("\n\r%i",choice);
                    break;
                }
                default:
                {
                    printf("\n\r%i",choice);
                    break;
                }
            }
        }
        switch(choice)
        {
            case 1:
            {

                if(tecla==80)
                {
                    clrscr();
                    menuCuerpoAbrir();
                    choice++;
                    break;
                }
                break;
            }
            case 2:
            {
                //printf("Opcion %d", choice);

                if(tecla==80)
                {
                    clrscr();
                    menuCuerpoModificar();
                    choice++;
                    break;
                }
                if(tecla==72)
                {
                    clrscr();
                    menuCuerpoRegistro();
                    choice--;
                    break;
                }
                break;
            }
            case 3:
            {
                //printf("Opcion %d", choice);
                if(tecla==80)
                {
                    clrscr();
                    menuCuerpoCerrar();
                    choice++;
                    break;
                }
                if(tecla==72)
                {
                    clrscr();
                    menuCuerpoAbrir();
                    choice--;
                    break;
                }
                break;
            }
            case 4:
            {
                //printf("Opcion %d", choice);
                if(tecla==72)
                {
                    clrscr();
                    menuCuerpoModificar();
                    choice--;
                }
                break;
            }
            default:
            {
                printf("error en algo");
                break;
            }
        }

    }while(loop==0);
    return 0;
}

